from ralgo.ralgo import Ralgo


def test_compress_basic1():
    message = ".....,,,,,,,...............,.......,......,,.......,.......,,.,............,.,,....,......,.......,,.......,,.....,..,.,..,,.......,......,,.......,...,,,.,,.....,........,.......,......,,....,,.,......,.,..............,.......,......,.......,,......,.,.....,.,......,"
    compressed = Ralgo(message).compress()

    assert (
        str(compressed)
        == "57{15}17162717211{12}112416172725121112271627133125181716242116111{14}17161726111511161"
    )


def test_compress_basic2():
    message = ".,,,,,,,.........,.,..,,,.......,......,"
    compressed = Ralgo(message).compress()

    assert str(compressed) == "179111237161"
